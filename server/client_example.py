def check_server_health():
    """Check if the server is running and healthy."""
    try:
        response = requests.get(f"{SERVER_URL}/health")
        if response.status_code == 200:
            data = response.json()
            print(f"‚úÖ Server is healthy: {data}")
            return True
        else:
            print(f"‚ùå Server health check failed: {response.status_code}")
            return False
    except requests.exceptions.ConnectionError:
        print("‚ùå Cannot connect to server. Make sure it's running on localhost:8000")
        return False

def initialize_safrole():
    """Initialize the safrole manager with the sample data."""
    try:
        print("üîÑ Initializing safrole manager...")
        response = requests.post(f"{SERVER_URL}/initialize", json=SAMPLE_DATA)
        
        if response.status_code == 200:
            data = response.json()
            print(f"‚úÖ Safrole manager initialized successfully!")
            print(f"   Current slot: {data['data']['current_slot']}")
            print(f"   Epoch length: {data['data']['epoch_length']}")
            print(f"   Submission period: {data['data']['submission_period']}")
            return True
        else:
            print(f"‚ùå Failed to initialize: {response.status_code}")
            print(f"   Error: {response.text}")
            return False
            
    except Exception as e:
        print(f"‚ùå Error during initialization: {str(e)}")
        return False

